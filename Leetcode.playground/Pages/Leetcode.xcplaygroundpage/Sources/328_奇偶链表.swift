    public func oddEvenList(_ head: ListNode?) -> ListNode? {
        if head == nil {
            return head
        }
        
        let evenHead = head?.next
        var odd = head
        var even = evenHead
        
        while even != nil, even?.next != nil {
            odd?.next = even?.next
            odd = odd?.next
            even?.next = odd?.next
            even = even?.next
        }
        odd?.next = evenHead
        
        return head
    }
