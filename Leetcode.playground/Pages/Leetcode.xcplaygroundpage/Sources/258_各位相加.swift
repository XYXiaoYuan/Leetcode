    public func addDigits(_ num: Int) -> Int {
        var num = num
        var sum = 0
        
        repeat {
            if num == 0 {
                num = sum
                sum = 0
            }
            let digital = num % 10
            sum += digital
            num /= 10
        } while num != 0 || sum >= 10
        
        return sum
    }
