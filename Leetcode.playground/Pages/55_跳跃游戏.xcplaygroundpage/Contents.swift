class Solution {
    func canJump(_ nums: [Int]) -> Bool {
        var k = 0
        for i in 0..<nums.count {
            if i > k {
                return false
            }
            
            k = max(k, i + nums[i])
        }
        
        return true
    }
}

let s = Solution()
let input1 = [2,3,1,1,4]
let input2 = [3,2,1,0,4]
let result = s.canJump(input1)
print(result)

assert(s.canJump(input1) == true)
assert(s.canJump(input2) == false)

